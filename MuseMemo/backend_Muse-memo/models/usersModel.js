import mongoose from "mongoose";

const Schema = mongoose.Schema

const usersSchema = new Schema({
    name: {
        type: String,
        required: true,
    },
    username: {
        type: String,
        unique: true,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    number: {
        type: String,
        required: true,
    },
    dateOfBirth: {
        type: Date,
        required: true,
    },
    gender: {
        type: String,
        enum:["male", "female", "other"],
        required: true,
    },

})


export default mongoose.model('Users', usersSchema)